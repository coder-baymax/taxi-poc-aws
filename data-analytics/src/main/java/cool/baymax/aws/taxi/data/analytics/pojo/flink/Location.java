	private static final TreeMap<String, Location> GEO_HASH_MAP = new TreeMap<>();

	static {
		for (Location location : LOCATIONS) {
			String geoHash = GeoHash.geoHashStringWithCharacterPrecision(location.lat, location.lng, 12);
			GEO_HASH_MAP.put(geoHash, location);
		}
	}

	public final int locationId;
	public final String borough;
	public final String zone;
	public final double lat;
	public final double lng;

	public Location(int locationId, String borough, String zone, double lat, double lng) {
		this.locationId = locationId;
		this.borough = borough;
		this.zone = zone;
		this.lat = lat;
		this.lng = lng;
	}

	public static Location getClosest(double lat, double lng) {
		String geoHash = GeoHash.geoHashStringWithCharacterPrecision(lat, lng, 12);
		Map.Entry<String, Location> l1 = GEO_HASH_MAP.floorEntry(geoHash);
		Map.Entry<String, Location> l2 = GEO_HASH_MAP.ceilingEntry(geoHash);
		return dis(lat, lng, l1) < dis(lat, lng, l2) ? l1.getValue() : l2.getValue();
	}

	public static void main(String[] args) {
		GeoHash hash = GeoHash.withBitPrecision(40.7643574, -73.92346189999999, 40);
		System.out.println(hash.longValue());
		GeoHash newHash = GeoHash.fromLongValue(hash.longValue(), 40);
		System.out.println(newHash.getPoint());
	}

	private static double dis(double lat, double lng, Map.Entry<String, Location> loc) {
		return loc == null
				? Double.MAX_VALUE
				: Math.pow(loc.getValue().lat - lat, 2) + Math.pow(loc.getValue().lng - lng, 2);
	}

